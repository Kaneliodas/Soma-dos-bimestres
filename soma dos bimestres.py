#Faça um Programa que peça as 4 notas bimestrais e mostre a média.
n1 = float(input("Digite sua nota no 1° bimestre: "))
n2 = float(input("Digite sua nota no 2° bimestre: "))
n3 = float(input("Digite sua nota no 3° bimestre: "))
n4 = float(input("Digite sua nota no 4° bimestre: "))

